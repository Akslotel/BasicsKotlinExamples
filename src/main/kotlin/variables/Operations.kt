// Функция для демонстрации операций с числами (арифметические и логические операции).
fun numberOperations(){
    val num1: Int = 8  // Переменная num1 хранит целое число 8
    val num2: Int = 4  // Переменная num2 хранит целое число 4

    // Арифметические операции
    println(num1 + num2) // Сложение (8 + 4 = 12)
    println(num1 - num2) // Вычитание (8 - 4 = 4)
    println(num1 * num2) // Умножение (8 * 4 = 32)
    println(num1 / num2) // Деление (8 / 4 = 2)
    println(num1 % num2) // Остаток от деления (8 % 4 = 0, остатка нет)

    // Логические операции для сравнения чисел
    println(num1 == num2)  // Равно ли? (false, потому что 8 != 4)
    println(num1 != num2)  // Не равно ли? (true, потому что 8 != 4)
    println(num1 < num2)   // Меньше ли 8 чем 4? (false)
    println(num1 > num2)   // Больше ли 8 чем 4? (true)
}

// Функция для демонстрации операций со строками.
fun textOperations(){

    val text1 = "Hello"  // Переменная text1 хранит строку "Hello"
    val text2 = "World"  // Переменная text2 хранит строку "World"

    // Конкатенация строк (соединение) с добавлением пробела между ними
    val text3 = text1 + " " + text2
    println(text3)  // Вывод: "Hello World"

    // Получение символа строки
    val letter = text3[0] // Индексация начинается с 0, так что это будет символ 'H'
    println(letter)  // Вывод: 'H'

    // Получение длины строки
    println(text3.length)  // Выводит длину строки (11 символов)

    // Интерполяция строк. Позволяет вставлять значения переменных внутрь строки.
    val name = "John"
    println("My name is $name")  // Вывод: "My name is John"
}

// Функция для демонстрации операций преобразования типов данных.
fun convertOperation(){
    // Преобразуем строку "42" в целое число с помощью метода toInt()
    val num = "42".toInt() // Переменная num хранит целое число 42

    // Преобразуем целое число обратно в строку с помощью метода toString()
    val text = num.toString() // Переменная text хранит строку "42"

    // Прибавляем к целому числу число с плавающей точкой (float), результат будет типа Float
    val floatType = num + 0.401f  // Переменная floatType хранит число 42.401 (Float)

    // Преобразуем значение float обратно в целое число
    val intType = floatType.toInt() // Переменная intType хранит значение 42 (дробная часть отбрасывается)

    // Преобразуем строку "3.14" в число типа Double
    val doubleType = "3.14".toDouble() // Переменная doubleType хранит число 3.14 (Double)

    // Вывод значений переменных в консоль
    println(num)         // Вывод: 42
    println(text)        // Вывод: "42"
    println(floatType)   // Вывод: 42.401
    println(intType)     // Вывод: 42
    println(doubleType)  // Вывод: 3.14
}