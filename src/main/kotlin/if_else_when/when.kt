package org.example.if_else_when

import kotlin.random.Random

/*
    Оператор when — это мощная альтернатива цепочкам if-else.
    Он используется для проверки одного значения на соответствие различным условиям.
    Кроме того, when позволяет проверять не только конкретные значения, но и произвольные условия.
    В отличие от if, where может проверять одно значение на несколько вариантов.
*/

fun main() {

}

fun basicWhenExamples() {
    val dayOfWeek = 3  // Пример фиксированного значения, обозначающего день недели

    // Пример 1: Простая проверка значения с помощью when
    when (dayOfWeek) {
        1 -> println("Понедельник")
        2 -> println("Вторник")
        3 -> println("Среда")
        4 -> println("Четверг")
        5 -> println("Пятница")
        6 -> println("Суббота")
        7 -> println("Воскресенье")
        else -> println("Некорректное значение")  // Обработка некорректного значения
    }

    val weather = "солнечно"

    // Пример 2: Использование when для строковых значений
    when (weather) {
        "солнечно" -> println("Сегодня солнечный день.")
        "дождливо" -> println("Сегодня дождь, возьмите зонтик.")
        "снежно" -> println("Сегодня снег, одевайтесь теплее.")
        else -> println("Погода не определена.")
    }
}

fun simpleWhenExamples() {
    // Генерируем случайные данные
    val age = Random.nextInt(10, 50)  // Случайный возраст от 10 до 50
    val hasDrivingLicense = Random.nextBoolean()  // Случайное наличие водительских прав (true или false)
    val isStudent = Random.nextBoolean()  // Случайный статус студента (true или false)

    // Выводим сгенерированные данные
    println("Возраст: $age")
    println("Есть водительские права: ${if (hasDrivingLicense) "Да" else "Нет"}")
    println("Студент: ${if (isStudent) "Да" else "Нет"}")

    // Пример 1: Простое использование when для проверки возраста
    when (age) {
        in 0..17 -> println("Вы несовершеннолетний.")
        in 18..65 -> println("Вы взрослый.")
        else -> println("Вы пенсионер.")
    }

    // Пример 2: Проверка наличия водительских прав
    when (hasDrivingLicense) {
        true -> println("У вас есть водительские права.")
        false -> println("У вас нет водительских прав.")
    }

    // Пример 3: Проверка статуса студента
    when (isStudent) {
        true -> println("Вы студент.")
        false -> println("Вы не студент.")
    }

    // Пример 4: Использование when как выражения
    // Оператор when можно использовать для возврата значения
    val category = when (age) {
        in 0..17 -> "Несовершеннолетний"
        in 18..65 -> "Взрослый"
        else -> "Пенсионер"
    }
    println("Категория: $category")
}
