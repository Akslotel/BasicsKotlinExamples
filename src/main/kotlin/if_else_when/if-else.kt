package org.example.if_else_when

import kotlin.random.Random

fun main(){
    moreIfExamples()
}

fun explainIfStatement() {
    // Оператор if используется для проверки условий.
    // Если условие истинно (true), выполняется блок кода внутри if.
    // Если условие ложно (false), можно использовать else для выполнения альтернативного блока кода.

    val number = readLine()!!.toInt()

    // Пример простого if-выражения
    if (number > 0) {
        println("Число $number является положительным.")  // Этот код выполнится, если number больше 0
    } else if (number < 0) {
        println("Число $number является отрицательным.")  // Этот код выполнится, если number меньше 0
    } else {
        println("Число $number равно нулю.")  // Этот код выполнится, если number равно 0
    }

    // Оператор if также может использоваться как выражение, возвращающее значение.
    // Например, мы можем присвоить переменной результат работы if:
    val result = if (number % 2 == 0) {
        "Число чётное"  // Это значение вернётся, если условие истинно
    } else {
        "Число нечётное"  // Это значение вернётся, если условие ложно
    }
    println(result)

    // Тернарное выражение if — это краткая запись для простой проверки.
    // Например, для определения наибольшего числа:
    val a = 5
    val b = 10
    val max = if (a > b) a else b  // Если a больше b, присвоится значение a, иначе b.
    println("Наибольшее число: $max")
}

fun moreIfExamples() {
    // Получаем возраст от пользователя
    print("Введите ваш возраст: ")
    val age = readLine()?.toIntOrNull() ?: 0  // Если введено некорректное значение, по умолчанию ставим 0

    // Получаем статус водительских прав от пользователя
    print("Есть ли у вас водительские права? (да/нет): ")
    val hasDrivingLicenseInput = readLine()?.trim()?.lowercase()
    val hasDrivingLicense = hasDrivingLicenseInput == "да"

    // Пример 1: Простой if-else
    if (age >= 18) {
        println("Вам $age лет, вы совершеннолетний.")
    } else {
        println("Вам $age лет, вы несовершеннолетний.")
    }

    // Пример 2: if-else if-else
    if (age < 18) {
        println("Вы ещё не можете водить машину.")
    } else if (age >= 18 && hasDrivingLicense) {
        println("Вы можете водить машину, так как у вас есть водительские права.")
    } else {
        println("Вы совершеннолетний, но у вас нет водительских прав.")
    }

    // Пример 3: Вложенный if
    if (age >= 18) {
        if (hasDrivingLicense) {
            println("Вы совершеннолетний и у вас есть водительские права.")
        } else {
            println("Вы совершеннолетний, но у вас нет водительских прав.")
        }
    } else {
        println("Вы несовершеннолетний.")
    }

    // Пример 4: Использование if как выражение для присвоения значения
    val votingStatus = if (age >= 18) "можете голосовать" else "не можете голосовать"
    println("Вы $votingStatus.")

    // Пример 5: Упрощённый тернарный оператор для проверки значения
    val permission = if (age >= 18) "Разрешено" else "Запрещено"
    println("Доступ: $permission")

    // Пример 6: Использование if без else
    if (age >= 18) {
        println("Вам можно принимать участие в выборах.")
    }
}

fun randomConditionsExamples() {
    // Генерируем случайные данные
    val age = Random.nextInt(10, 50)  // Случайный возраст от 10 до 50
    val hasDrivingLicense = Random.nextBoolean()  // Случайное наличие водительских прав (true или false)
    val hasJob = Random.nextBoolean()  // Случайная занятость (true или false)
    val isStudent = Random.nextBoolean()  // Случайный статус студента (true или false)

    // Выводим сгенерированные данные
    println("Возраст: $age")
    println("Есть водительские права: ${if (hasDrivingLicense) "Да" else "Нет"}")
    println("Есть работа: ${if (hasJob) "Да" else "Нет"}")
    println("Студент: ${if (isStudent) "Да" else "Нет"}")

    // Пример 1: Использование && (логическое И)
    // Оператор && проверяет, истинны ли оба условия.
    // Только если оба условия истинны (age >= 18 и hasDrivingLicense == true), выполняется блок if.
    if (age >= 18 && hasDrivingLicense) {
        println("Вы старше 18 лет и можете водить машину.")
    } else {
        println("Вы не можете водить машину по одному из условий: либо вам меньше 18, либо у вас нет прав.")
    }

    // Пример 2: Использование || (логическое ИЛИ)
    // Оператор || проверяет, истинно ли хотя бы одно из условий.
    // Если хотя бы одно из условий (hasJob или isStudent) истинно, выполняется блок if.
    if (hasJob || isStudent) {
        println("Вы имеете право на льготы, так как либо у вас есть работа, либо вы студент.")
    } else {
        println("Вы не имеете права на льготы, так как вы не студент и у вас нет работы.")
    }

    // Пример 3: Комбинированное использование && и ||
    // Проверяем, если человек старше 18 лет и у него есть либо работа, либо водительские права.
    // Здесь используется && для проверки возраста и || для проверки работы или прав.
    if (age >= 18 && (hasJob || hasDrivingLicense)) {
        println("Вы совершеннолетний и имеете либо работу, либо водительские права.")
    } else {
        println("Вы либо несовершеннолетний, либо у вас нет работы и водительских прав.")
    }

    // Пример 4: Использование нескольких условий с логическими операторами
    // Проверяем, если человек старше 18 лет, не является студентом и имеет водительские права.
    // Используем && для проверки всех условий.
    if (age >= 18 && !isStudent && hasDrivingLicense) {
        println("Вы совершеннолетний, не студент и имеете водительские права.")
    } else {
        println("Вы не подходите под все условия: возможно, вы студент, младше 18 или без водительских прав.")
    }

    // Пример 5: Использование || для альтернативных условий
    // Оператор || проверяет, истинно ли хотя бы одно условие: человек младше 18 или является студентом.
    if (age < 18 || isStudent) {
        println("Вы младше 18 лет или являетесь студентом.")
    } else {
        println("Вы старше 18 лет и не являетесь студентом.")
    }
}